<h5 class="fw-bold mb-3">Mengelola Profil</h5>

<p class="text-muted">
    Menu Profil digunakan untuk memperbarui data akun pengguna.
    Data profil yang benar membantu sistem mengenali identitas pengguna dengan lebih baik.
</p>

<ul class="text-muted">
    <li>Pengguna dapat mengubah nama lengkap.</li>
    <li>Pengguna dapat memperbarui alamat email.</li>
    <li>Username tidak dapat diubah.</li>
    <li>Password baru dapat diisi jika pengguna ingin mengganti password.</li>
    <li>Jika tidak ingin mengganti password, kolom password baru dapat dikosongkan.</li>
</ul>