<h5 class="fw-bold mb-3">Langkah Membuat Pengaduan</h5>

<p class="text-muted">
    Menu Buat Pengaduan digunakan untuk mengirim laporan baru kepada sistem.
    Pastikan data yang dimasukkan jelas agar pengaduan mudah dipahami dan diproses.
</p>

<ol class="text-muted">
    <li>Buka menu <strong>Buat Pengaduan</strong>.</li>
    <li>Isi judul pengaduan dengan singkat dan jelas.</li>
    <li>Pilih kategori sesuai jenis masalah yang dilaporkan.</li>
    <li>Tuliskan lokasi kejadian secara lengkap.</li>
    <li>Jelaskan isi pengaduan dengan detail.</li>
    <li>Unggah file bukti yang sesuai.</li>
    <li>Klik tombol <strong>Kirim Pengaduan</strong>.</li>
</ol>