<h5 class="fw-bold mb-3">Melihat Pengaduan Saya</h5>

<p class="text-muted">
    Menu Pengaduan Saya digunakan untuk melihat daftar pengaduan yang sudah dikirim.
    Pengguna dapat memantau status dan membuka detail pengaduan dari halaman ini.
</p>

<ul class="text-muted">
    <li>Pengguna dapat melihat kode pengaduan.</li>
    <li>Pengguna dapat melihat judul, kategori, status, dan tanggal pengaduan.</li>
    <li>Pengguna dapat membuka detail pengaduan untuk melihat informasi lengkap.</li>
    <li>Pengguna dapat menggunakan fitur pencarian untuk menemukan pengaduan tertentu.</li>
    <li>Pengguna dapat melihat perkembangan status pengaduan.</li>
</ul>