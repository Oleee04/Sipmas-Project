<h5 class="fw-bold mb-3">Ketentuan Mengunggah Bukti</h5>

<p class="text-muted">
    File bukti digunakan untuk mendukung laporan pengaduan yang dikirimkan.
    Bukti dapat berupa gambar atau dokumen yang berkaitan dengan masalah yang dilaporkan.
</p>

<ul class="text-muted">
    <li>Format file yang diperbolehkan adalah JPG, JPEG, PNG, dan PDF.</li>
    <li>Ukuran maksimal file adalah 2 MB.</li>
    <li>File harus sesuai dengan isi pengaduan.</li>
    <li>File yang tidak valid tidak akan diterima oleh sistem.</li>
    <li>Pastikan bukti yang diunggah dapat memperjelas laporan.</li>
</ul>